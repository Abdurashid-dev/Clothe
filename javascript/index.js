$('.brands').click(function(){
    let name = this.innerHTML
    brand(name);
})

function brand(title){
    let brandArr = [];
    if(title == 'All'){
        brandArr = data
    } else{
        brandArr = data.filter((elem)=>{
            if(elem.title.includes(title)){
                return true
            } else{
                return false
            }
        })
    }
    buildProduct(brandArr);
}

